import re


def solution(word, pages):
    answer = {}

    for i, page in enumerate(pages):
        domain = re.search(r'<meta[^>]+ content="https://(\S+)"/>', page).group(1)
        base_score = re.findall(r'[a-z]+', page.lower()).count(word.lower())
        links = re.findall(r'<a href="https://(\S+)">', page)
        answer[domain] = [i, base_score, links, 0]

    for domain, values in answer.items():
        i, base_score, links, _ = values

        for link in links:
            if link in answer:
                answer[link][3] += base_score / len(links)

    return sorted(answer.values(), key=lambda item: item[1] + item[3], reverse=True)[0][0]


